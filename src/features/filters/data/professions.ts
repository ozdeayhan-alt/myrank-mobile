function buildProfessionList(): readonly string[] {
  const pinnedSet = new Set<string>(PINNED_PROFESSIONS);
  const deduped = ALPHABETICAL_PROFESSIONS.filter(
    (item) => !pinnedSet.has(item)
  );
  const unique = [...new Set(deduped)].sort((a, b) =>
    a.localeCompare(b, "tr-TR")
  );
  return [...PINNED_PROFESSIONS, ...unique, "Diğer"];
}

/** Profil, kayıt sonrası onboarding ve filtreler — tek kaynak. */
export const PROFESSIONS: readonly string[] = buildProfessionList();

export function getProfessionOptions(): string[] {
  return [...PROFESSIONS];
}
